package main

import "fmt"

func main() {
	// Declaring and initializing float variables
	pi := 3.14
	radius := 5.0

	// Printing the floats
	fmt.Println("Pi:", pi)
	fmt.Println("Radius:", radius)

	// Calculating the area of a circle
	area := pi * (radius * radius)
	fmt.Println("Area of Circle:", area)
}
