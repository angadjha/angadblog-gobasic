package main

import "fmt"

func main() {

	// Declare an integer variable
	var myInteger int

	// Assign a value to the integer variable
	myInteger = 42

	// Print the value of the integer
	fmt.Println("My integer:", myInteger)

	// Perform some operations with integers
	result := myInteger + 10
	fmt.Println("Result of addition:", result)

	// Update the integer value
	myInteger = myInteger * 2
	fmt.Println("Updated integer value:", myInteger)

}
